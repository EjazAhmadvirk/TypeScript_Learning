let grade: string = "B";
switch(grade) {
    case "A":
        console.log("Excellent!");
        break;
    case "B":
        console.log("Good job!");
        break;
    case "C":
        console.log("You can do better.");
        break;
    default:
        console.log("Keep trying!");
}
// Output: Good job!
